"""D-015 裁定のための数値検証スクリプト（Fable5, 2026-07-09）。

憲法第2章-5（数値の再計算は必ずPythonで行う）に基づき、D-015の3論点の根拠計算を
再現可能な形で残す。実行: python3 outputs/review/2026-07-09-d015-risk-calculations.py

前提値の出典:
- 資金300万円・1銘柄上限10%・損切り-10%・利確+25%・セクター40%: corp/board.md D-002
- 同時保有上限5銘柄・確信度連動 高10%/中5%/低3%: corp/board.md D-011
- 実効値（税・スリッページ・平均ギャップ込み）勝ち+19.42%/負け-12.5%・実務分岐勝率39.2%:
  outputs/review/2026-07-09-fable5-strategy-review.md §5-1
- 壊滅的ギャップの発生確率（負けトレードのうち4%が-30%、1%が-80%）は**実測データの
  存在しない仮定値**である（第2条: 出典なし数値のため「仮定」と明記。感度分析で幅を示す）。
"""

import math
import random
import statistics

C0 = 3_000_000          # 当初資金（D-002）
W_EFF = 0.1942          # 勝ちの実効値（税・スリッページ込み、戦略レビュー§5-1）
L_EFF = 0.125           # 負けの実効値（平均ギャップ-12%+スリッページ、同上）

SEP = "=" * 72


def sec(title):
    print(f"\n{SEP}\n{title}\n{SEP}")


# ----------------------------------------------------------------------
sec("論点1: 同時保有ポジションの相関リスク（最悪ケース同時損失）")

# 露出パターン: (説明, 各ポジションの投入比率リスト)
patterns = [
    ("構造上限: 高確信5銘柄", [0.10] * 5),
    ("検証フェーズ想定例: 高2+中2+低1", [0.10, 0.10, 0.05, 0.05, 0.03]),
    ("控えめ例: 中2+低1", [0.05, 0.05, 0.03]),
]
print(f"{'パターン':<28}{'露出':>7}{'同時損切り-10%':>14}{'同時ギャップ-25%':>16}")
for name, ws in patterns:
    expo = sum(ws)
    stop = expo * 0.10   # 全ポジションが同時に-10%損切り
    crash = expo * 0.25  # マクロショック仮定: 全ポジション同時に-25%で約定（仮定値）
    print(f"{name:<28}{expo*100:>6.1f}%{stop*100:>12.2f}%{crash*100:>14.2f}%")

worst_stop = 5 * 0.10 * 0.10
worst_crash = 5 * 0.10 * 0.25
print(f"\n構造上限での最悪ケース同時損切り: {worst_stop*100:.2f}% = "
      f"{C0*worst_stop:,.0f}円 → 回復必要リターン +{worst_stop/(1-worst_stop)*100:.2f}%")
print(f"構造上限での同時-25%ギャップ(仮定): {worst_crash*100:.2f}% = "
      f"{C0*worst_crash:,.0f}円 → 回復必要リターン +{worst_crash/(1-worst_crash)*100:.2f}%")
print("→ PML(ポートフォリオ想定最大同時損失) = Σ(投入比率×10%) ≤ 5.0% を明文の機械照合とする")

# ----------------------------------------------------------------------
sec("論点2: ギャップ・約定リスクのテール（壊滅的ギャップ）")

print("単一銘柄の壊滅シナリオ（投入比率別・資金全体への影響）:")
print(f"{'投入比率':>8} {'-30%':>10} {'-50%':>10} {'-100%(上場廃止)':>16}")
for f in (0.10, 0.05, 0.03):
    row = [f * g for g in (0.30, 0.50, 1.00)]
    print(f"{f*100:>7.0f}% " + " ".join(f"{x*100:>9.2f}%" for x in row)
          + f"   (= {C0*row[2]:,.0f}円)")
loss10 = 0.10
print(f"\n最悪（10%枠が-100%）: 資金の-{loss10*100:.1f}% → 回復必要リターン "
      f"+{loss10/(1-loss10)*100:.2f}% → 破産（資金の枯渇）には至らない")

print("\nテール混在時の実務損益分岐勝率（p* = E[負け]/(E[勝ち]+E[負け])）:")
print("  負けトレードの損失分布の仮定（実測なし・感度分析）:")
scenarios = [
    ("テールなし（レビュー§5-1と同一: 全負け-12.5%）", [(1.00, 0.125)]),
    ("弱テール: 負けの2%が-30%", [(0.98, 0.125), (0.02, 0.30)]),
    ("基本仮定: 負けの4%が-30%・1%が-80%", [(0.95, 0.125), (0.04, 0.30), (0.01, 0.80)]),
    ("強テール: 負けの8%が-30%・2%が-80%", [(0.90, 0.125), (0.08, 0.30), (0.02, 0.80)]),
]
for name, dist in scenarios:
    el = sum(p * l for p, l in dist)
    pstar = el / (W_EFF + el)
    print(f"  {name:<44} E[負け]={el*100:5.2f}% → p*={pstar*100:5.1f}%")
print("→ テールは分岐勝率を約39%→40〜43%へ押し上げる。シャドー検証の参照分岐帯を")
print("  「33〜39%（テールなし）+ テール込み目安〜42%」として注記する")

# ----------------------------------------------------------------------
sec("論点3: サイジングの複利方式 — Kelly基準による現行10%枠の位置づけ")

# 2値アウトカム(+w, -l)のKelly比率: f* = p/l - q/w
print(f"実効値 勝ち+{W_EFF*100:.2f}% / 負け-{L_EFF*100:.1f}% のときのKelly最適比率:")
for p in (0.35, 0.392, 0.40, 0.42, 0.45, 0.50):
    f_star = p / L_EFF - (1 - p) / W_EFF
    print(f"  勝率 {p*100:5.1f}% → Kelly f* = {f_star*100:7.2f}%"
          + ("（賭けるべきでない）" if f_star <= 0 else ""))
print("→ 勝率40%（実務分岐39.2%をわずかに超える水準）でKelly≈11%。すなわち現行の")
print("  高確信10%枠は、勝率が未実証の現在、実質フルKelly級の攻撃的サイズである。")
print("  固定額方式でドローダウン時に実効比率が10%を超えて膨らむ（下記）ことは、")
print("  Kelly超過＝長期成長率の毀損と破産リスク増大を意味する。")

drawdowns = (0.0, 0.15, 0.30, 0.50)
print("\n固定額方式（当初資金基準30万円固定）の実効投入比率のクリープ:")
for dd in drawdowns:
    eq = C0 * (1 - dd)
    print(f"  資金残高 {eq:>9,.0f}円（DD{dd*100:3.0f}%）: 30万円は資金の {300_000/eq*100:5.1f}%")
print("比例方式は常に10.0%（DD後は自動縮小: 例 DD30%後の10%枠 = "
      f"{C0*0.7*0.10:,.0f}円）")

n_half_prop = math.log(0.5) / math.log(1 - 0.10 * L_EFF)
n_half_fix = (C0 / 2) / (C0 * 0.10 * L_EFF)
print(f"\n資金が半減するまでの連続負け数（1ポジション逐次・負け-12.5%・10%枠）:")
print(f"  比例方式: {n_half_prop:.0f}連敗（数学的にゼロ到達不能）")
print(f"  固定額方式: {n_half_fix:.0f}連敗（さらに同ペースで枯渇へ直進）")

# ----------------------------------------------------------------------
sec("論点3: モンテカルロ比較（固定額 vs 比例、テール込み）")

random.seed(20260709)  # 再現性のため固定
N_SIMS = 20_000
FRAC = 0.10

# 負け内訳（論点2の基本仮定）: 通常-12.5% 95% / -30% 4% / -80% 1%
def draw_loss(rng):
    u = rng.random()
    if u < 0.95:
        return 0.125
    if u < 0.99:
        return 0.30
    return 0.80

def simulate(p_win, n_trades, proportional, rng):
    eq = C0
    peak = C0
    min_eq = C0
    for _ in range(n_trades):
        bet = FRAC * (eq if proportional else C0)
        bet = min(bet, eq)          # 資金以上は賭けられない
        if eq <= 0:
            break
        if rng.random() < p_win:
            eq += bet * W_EFF
        else:
            eq -= bet * draw_loss(rng)
        peak = max(peak, eq)
        min_eq = min(min_eq, eq)
    return eq, min_eq

def run(p_win, n_trades):
    rng = random.Random(f"{p_win}-{n_trades}")
    rows = {}
    for prop in (False, True):
        finals, mins = [], []
        for _ in range(N_SIMS):
            f, m = simulate(p_win, n_trades, prop, rng)
            finals.append(f)
            mins.append(m)
        finals.sort()
        ruin50 = sum(1 for m in mins if m < C0 * 0.5) / N_SIMS
        ruin70 = sum(1 for m in mins if m < C0 * 0.7) / N_SIMS
        rows["比例" if prop else "固定"] = (
            statistics.median(finals) / C0,
            finals[int(N_SIMS * 0.05)] / C0,
            ruin70, ruin50,
        )
    print(f"\n勝率{p_win*100:.0f}%・{n_trades}トレード（{N_SIMS:,}試行、負けテール込み）:")
    print(f"  {'方式':<4}{'中央値(倍)':>12}{'下位5%(倍)':>12}{'P(最低残高<70%)':>16}{'P(最低残高<50%)':>16}")
    for k, (med, p5, r70, r50) in rows.items():
        print(f"  {k:<4}{med:>11.3f}{p5:>12.3f}{r70*100:>14.2f}%{r50*100:>14.2f}%")

# 検証フェーズ想定（約102判定に対応する実トレード数は少ないが、比較のため両方）
for p_win in (0.30, 0.392, 0.45):
    for n in (102, 500):
        run(p_win, n)

print("""
読み方:
- 勝率30%（分岐以下=エッジ無し）: どちらも減るが、固定額は下振れ（P(残高<50%)）が
  比例より一桁大きい。検証段階で最も守るべきはこのケース。
- 勝率39.2%（分岐ちょうど）: 期待値ゼロ近傍。固定額の破滅テールだけが残る。
- 勝率45%（エッジ実証後）: 中央値成長は同等以上で、比例が下振れ耐性でも優位。
→ 「資産最大化」と「破産回避」の両立の観点で、比例（現在資金残高基準）を採用する。
""")
